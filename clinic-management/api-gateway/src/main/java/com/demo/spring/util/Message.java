package com.demo.spring.util;

public class Message {
	
	private String status;
	
	public Message() {
		
	}

	public Message(String status) {
		super();
		this.status = status;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	
	

}
